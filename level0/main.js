// var is a type of variable in javascript that can be reassigned and redeclared.
// var is a global variable that can be accessed from anywhere in the code.
// var is a function-scoped variable that can be accessed within the function it is declared in.
// var is a block-scoped variable that can be accessed within the block it is declared in.
// var is a type of variable in javascript that can be reassigned and redeclared.



//  let is a type of variable in javascript that can be reassigned but not redeclared.
//  let is a global variable that can be accessed from anywhere in the code.
//  let is a function-scoped variable that can be accessed within the function it is declared in.
//  let is a block-scoped variable that can be accessed within the block it is declared in.
//  let is a type of variable in javascript that can be reassigned but not redeclared.



//  const is a type of variable in javascript that cannot be reassigned or redeclared.
//  const is a global variable that can be accessed from anywhere in the code.
//  const is a function-scoped variable that can be accessed within the function it is declared in.
//  const is a block-scoped variable that can be accessed within the block it is declared in.
//  const is a type of variable in javascript that cannot be reassigned or redeclared.



